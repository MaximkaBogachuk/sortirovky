def shell_sort(arr):
    n = len(arr)
    # начинаем с большого шага и постепенно уменьшаем его
    gap = n // 2
    while gap > 0:
        # проходим по подмассиву, состоящему из элементов, находящихся на расстоянии gap друг от друга
        for i in range(gap, n):
            # сохраняем текущий элемент и его индекс
            temp = arr[i]
            j = i
            # сдвигаем элементы влево, пока не найдем правильную позицию для вставки текущего элемента
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap
            # вставляем текущий элемент в правильную позицию
            arr[j] = temp
        # уменьшаем шаг
        gap //= 2
